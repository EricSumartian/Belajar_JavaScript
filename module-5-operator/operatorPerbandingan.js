/** Operator Perbandingan */

// == mengecek / membandingkan persamaan nilai
// let a = "5"
// let b = 5 
// console.log( a == b) -> true, karena nilai 5 sama meskipun tipe data berbeda

// === mengecek / membandingkan persamaan nilai dan tipe data
// let a = "5" -> string
// let b = 5 -> number
// console.log( a === b) -> false, karena tipe data berbeda 

//  != mengecek ketisaksamaan nilai dari variable
// let a = "5"
// let b = 5 
// console.log( a != b) 

// !== mengecek ketidaksamaan nilai dan tipe data
// let a = "5"
// let b = 5 
// console.log( a !== b) 

// > mengecek apakah variable satu lebih besar dari variable lain
// let a = 7 
// let b = 5 
// console.log( a > b) 

// < mengecek apakah variable satu lebih kecil dari variable lain
// let a = 3
// let b = 5 
// console.log( a < b) 

// >= mengecek apakah variable satu lebih besar atau sama dengan variable lain
// let a = 10
// let b = 5 
// console.log( a >= b) 

// <= mengecek apakah variable satu lebih kecil atau sama dengan variable lain
// let a = 3
// let b = 5 
// console.log( a <= b) 
